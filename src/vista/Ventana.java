    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        
        XYSeries series = new XYSeries("F1");
        XYSeries series2 = new XYSeries("F2");

        // Introduccion de datos
        for(int i =0; i<20; i++){
        
            series.add(i,f1.f(x1, y1, c1, i) );
            series2.add(i,f1.f(x2, y2, c2, i) );
        }
        
        
       
        XYSeriesCollection dataset = new XYSeriesCollection();
        dataset.addSeries(series);
        dataset.addSeries(series2);

        JFreeChart chart = ChartFactory.createXYLineChart(
                "Graficas", // TÃ­tulo
                "X", // Etiqueta Coordenada X
                "Y", // Etiqueta Coordenada Y
                dataset, // Datos
                PlotOrientation.VERTICAL,
                true, // Muestra la leyenda de los productos (Producto A)
                false,
                false
        );

        // Mostramos la grafica en pantalla
        ChartFrame frame = new ChartFrame("Grafica Lineal", chart);
        frame.pack();
        frame.setVisible(true);
        
        // TODO add your handling code here:
    }//GEN-LAST:event_jButton2ActionPerformed

